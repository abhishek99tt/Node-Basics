const os = require('os');

// Platform
console.log(os.platform());

// Architecture
console.log(os.arch());

// CPU info
console.log(os.cpus());

// Free memory
console.log(os.freemem());

// Total memory
console.log(os.totalmem());

// Home dir
console.log(os.homedir());

// Uptime
console.log(os.uptime());